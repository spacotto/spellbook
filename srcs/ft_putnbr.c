#include "libft.h"

void	ft_putnbr(int nb)
{
	unsigned int	nba;

	nba = nb;
	if (nb < 0)
	{
		ft_putchar('-');
		nba = -nb;
	}
	if (nba > 9)
	{
		ft_putnbr(nba / 10);
		ft_putnbr(nba % 10);
	}
	else
		ft_putchar(nba + '0');
}
