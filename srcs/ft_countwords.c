#include "libft.h"

int	ft_countwords(char const *s, char c)
{
	size_t	i;	
	size_t	counter;

	i = 0;
	if (s[i] != c)
		counter = 1;
	else
		counter = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c && s[i + 1] != c && s[i + 1] != '\0')
			counter++;
		i++;
	}
	return (counter);
}
